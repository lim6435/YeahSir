package com.lsg.www.vo;

public class YsCoptVo {
    private String coptId;
    private String coptName;
    private String coptLoc;
    private String coptDt;
    private String coptTm;
    private String coptPrid;
    private String coptAgeGrop;
    private String coptMtchRule;
    private String regId;

    public String getCoptId() {
        return coptId;
    }

    public void setCoptId(String coptId) {
        this.coptId = coptId;
    }

    public String getCoptName() {
        return coptName;
    }

    public void setCoptName(String coptName) {
        this.coptName = coptName;
    }

    public String getCoptLoc() {
        return coptLoc;
    }

    public void setCoptLoc(String coptLoc) {
        this.coptLoc = coptLoc;
    }

    public String getCoptDt() {
        return coptDt;
    }

    public void setCoptDt(String coptDt) {
        this.coptDt = coptDt;
    }

    public String getCoptTm() {
        return coptTm;
    }

    public void setCoptTm(String coptTm) {
        this.coptTm = coptTm;
    }

    public String getCoptPrid() {
        return coptPrid;
    }

    public void setCoptPrid(String coptPrid) {
        this.coptPrid = coptPrid;
    }

    public String getCoptAgeGrop() {
        return coptAgeGrop;
    }

    public void setCoptAgeGrop(String coptAgeGrop) {
        this.coptAgeGrop = coptAgeGrop;
    }

    public String getCoptMtchRule() {
        return coptMtchRule;
    }

    public void setCoptMtchRule(String coptMtchRule) {
        this.coptMtchRule = coptMtchRule;
    }

    public String getRegId() {
        return regId;
    }

    public void setRegId(String regId) {
        this.regId = regId;
    }
}
